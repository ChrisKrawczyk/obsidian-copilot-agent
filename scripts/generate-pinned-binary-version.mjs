#!/usr/bin/env node
/**
 * Generates src/sdk/pinnedBinaryVersion.ts from the installed
 * @github/copilot package version. Run by package.json hooks
 * (postinstall, pretest, pretypecheck, prebuild) so the generated
 * file is always present before tests/typecheck/build/etc.
 *
 * Idempotent: only writes when content would change. Falls back to
 * "0.0.0" with a console warning if @github/copilot/package.json is
 * unreadable (e.g. mid-install) so downstream commands never fail
 * purely due to the generated module being absent.
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { join, resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = resolve(fileURLToPath(import.meta.url), "..", "..");
const pkgJsonPath = join(repoRoot, "node_modules", "@github", "copilot", "package.json");
const outputPath = join(repoRoot, "src", "sdk", "pinnedBinaryVersion.ts");

function readVersion() {
  if (!existsSync(pkgJsonPath)) {
    console.warn(
      `generate-pinned-binary-version: ${pkgJsonPath} not found, using fallback "0.0.0"`,
    );
    return "0.0.0";
  }
  try {
    const raw = JSON.parse(readFileSync(pkgJsonPath, "utf8"));
    const v = typeof raw?.version === "string" ? raw.version : null;
    if (!v) {
      console.warn(
        "generate-pinned-binary-version: package.json has no version field, using fallback \"0.0.0\"",
      );
      return "0.0.0";
    }
    return v;
  } catch (err) {
    console.warn(
      `generate-pinned-binary-version: failed to parse package.json (${err instanceof Error ? err.message : String(err)}), using fallback "0.0.0"`,
    );
    return "0.0.0";
  }
}

const version = readVersion();
const body =
  "// AUTO-GENERATED by scripts/generate-pinned-binary-version.mjs — do not edit.\n" +
  `export const PINNED_BINARY_VERSION = ${JSON.stringify(version)};\n`;

const existing = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : null;
if (existing === body) {
  // Idempotent no-op
  process.exit(0);
}

writeFileSync(outputPath, body, "utf8");
console.log(`generate-pinned-binary-version: wrote ${outputPath} (v${version})`);
